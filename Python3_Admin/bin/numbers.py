#!/usr/bin/env python3.7

print("# PART I - Numbers\n")
print("# Addition\n2 + 2\n4\n")
print("# Subtraction\n10 - 4\n6\n")
print("# Multiplication\n3 * 9\n27\n")
print("# Division\n5 / 3\n1.66666666666667\n")
print("# Floor division, always returns a number without a remainder\n5 // 3\n1\n")
print("# Modulo division, returns the remainder\n8 % 3\n2\n")
print("# Exponent\n2 ** 3\n8\n\n")

print("# PART II - Converting Strings and Numbers\n")
print("str(1.1)\n\'1.1\'\n")
print("int(\"10\")\n10\n")
print("int(5.99999)\n5\n")
print("float(\"5.6\")\n5.6\n")
print("float(5)\n5.0\n\n")

print("# PART III - Use type() function to get value's property\n")
print("type(\"This\")\n<class \'str\'>\n")
